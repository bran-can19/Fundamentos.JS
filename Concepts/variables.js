// DECLARACIÓN DE VARIABLES CON LET Y CONST
// VAR: en desuso

//variables  let: cambiarán de valor
let age; // declaración
console.log(age);
age = 23; // inicialización
console.log(age);

let name = "Francisco"; // declaración d inicializacióm
console.log(name);
name = "Brandon";
console.log(name);

// constante const: no cambiaran de valor
const PI = 3.1416;
//PI = 3.15;
console.log(PI);